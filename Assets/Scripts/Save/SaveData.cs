[System.Serializable]
public class SaveData
{
    public int Player_LV;            // プレイヤーレベル
    public int Player_LP;            // プレイヤーのレベルポイント
    public float Player_EXP;         // プレイヤー経験値
    public float Player_NEXT_EXP;    // プレイヤーの次の経験値
    public float Player_HP_Max;          // プレイヤーのHP
    public float Player_MP_Max;          // プレイヤーのMP
    public float Player_SP;          // プレイヤーのスタミナ
    public float Player_PA;          // プレイヤーの物攻
    public float Player_MA;          // プレイヤーの魔攻
    public float Player_DF;          // プレイヤーの防御力
    public float Player_RT;          // プレイヤーの抵抗力
    public float Player_SPEED;       // プレイヤーのスピード

    public float Player_HP_Now;          // プレイヤーの現在のHP
    public float Player_MP_Now;          // プレイヤーの現在のMP
}